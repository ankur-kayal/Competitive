#define nl '\n'

void run_cases() {
    int N;
    cin >> N;
    vector<pair<int64_t, int64_t>> A(N);
    for(int i=0;i<N;i++) {
        int64_t start, end;
        cin >> start >> end;
        A[i] = {start, end};
    }
    sort(A.begin(), A.end());

    int64_t start = A[0].first;
    int ans = 0;
    for(int i=0;i<N;i++) {
        if(start < A[i].first) {
            start = A[i].first;
        }
        if(A[i].first <= start && start <= A[i].second) {
            ans++;
            start++;
        }
    }

    cout << ans << '\n';
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(nullptr);

    int tests = 1;
    cin >> tests;

    for(int test = 1;test <= tests;test++) {
        run_cases();
    }
}